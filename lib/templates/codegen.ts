export function generateProjectFiles(bp: Blueprint): AppFile[] {
  const used = usedSectionTypes(bp);

  const files: AppFile[] = [
    { path: "package.json", content: packageJson(bp) },
    { path: "vite.config.ts", content: VITE_CONFIG },
    { path: "tsconfig.json", content: TSCONFIG },
    { path: "index.html", content: indexHtml(bp) },
    { path: "postcss.config.js", content: POSTCSS },
    { path: "tailwind.config.js", content: tailwindConfig(bp) },
    { path: "src/main.tsx", content: MAIN_TSX },
    { path: "src/index.css", content: indexCss(bp) },
    { path: "src/App.tsx", content: appTsx(bp) },
    { path: "src/types.ts", content: typesFile() },
    { path: "src/data/blueprint.ts", content: blueprintData(bp) },
    { path: "src/components/Layout.tsx", content: COMPONENT_SOURCES["Layout.tsx"] },
    { path: "src/components/SectionRenderer.tsx", content: sectionRenderer(used) },
    { path: "README.md", content: readme(bp) },
  ];

  for (const t of Array.from(used)) {
    const c = SECTION_COMPONENT_MAP[t];
    files.push({
      path: `src/components/${c.file}`,
      content: COMPONENT_SOURCES[c.file],
    });
  }

  if (bp.features.supabase.enabled) {
    files.push({ path: "src/lib/supabase.ts", content: supabaseClient() });
    files.push({
      path: ".env.example",
      content: "VITE_SUPABASE_URL=\nVITE_SUPABASE_ANON_KEY=\n",
    });
  }

  return files.sort((a, b) => a.path.localeCompare(b.path));
}
