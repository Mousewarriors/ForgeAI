function sectionRenderer(used: Set<SectionType>): string {
  const imports = Array.from(used)
    .map((t) => {
      const c = SECTION_COMPONENT_MAP[t];
      return `import ${c.name} from "./${c.file.replace(".tsx", "")}";`;
    })
    .join("\n");
  const cases = Array.from(used)
    .map((t) => {
      const c = SECTION_COMPONENT_MAP[t];
      return `    case "${t}":\n      return <${c.name} section={section} />;`;
    })
    .join("\n");
  return COMPONENT_SOURCES["SectionRenderer.tsx"]
    .replace("__IMPORTS__", imports)
    .replace("__CASES__", cases);
}

