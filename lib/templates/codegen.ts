function usedSectionTypes(bp: Blueprint): Set<SectionType> {
  const set = new Set<SectionType>();
  for (const page of bp.pages) for (const s of page.sections) set.add(s.type);
  if (bp.features.auth) set.add("login");
  return set;
}

